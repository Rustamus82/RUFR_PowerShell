    [CmdletBinding()] 
    param( 
        [Parameter(Mandatory=$false, Position=0, ValueFromPipelineByPropertyName,
		HelpMessage="Angiv Drevbogstav der skal checkes for  'Log4J' eller 'slf4j' ")] 
        [string] $drev="c"
        ,[Parameter(Mandatory=$false, Position=1, ValueFromPipelineByPropertyName,
		HelpMessage="Angiv j for at checke om der er installeret Java")] 
        [string] $Java="n"
		)
Begin{
	if(!($drev.length -gt 0)){$drev="c:"}else {$drev=$drev.substring(0,1)+":"}
	If (test-path $drev){Write-host "Drev: $drev fundet, fortsætter."}Else{Write-host "Drev: $drev ikke fundet, farvel!";Exit}
	$Global:EgoIsAdm=[bool](([System.Security.Principal.WindowsIdentity]::GetCurrent()).groups -match "S-1-5-32-544")
	If(!($EgoIsAdm)) {
		Write-Output "Du skal køre powershell som Administrator for at kunne slette / omdøbe låste filer på Server"
		Exit
	}
	$Global:EgoCrLf="`r`n" 
	$repl="l4jrepl"
	$rm1="log4j"
	$rm2="slf4j"
	$antL=12
	$retur=@()
	$core=@()
	if(($Java.length -gt 0) -and ($Java.substring(0,1) -eq "j")){
		$Javaf=0
		$Prg=Get-ItemProperty HKLM:\Software\Wow6432Node\Microsoft\Windows\CurrentVersion\Uninstall\* | Select-Object DisplayName, DisplayVersion, Publisher, InstallDate 
		foreach ($p in $Prg){
			If(($p.DisplayName -match("java")) -or ($p.DisplayName -match("OpenJDK"))){
				$Javaf+=1;
				$retur+="#Java / OpenJDK Fundet: "+$p.DisplayName +" version: "+$p.DisplayVersion +" fra: "+$p.Publisher
			}
		}
		If($Javaf -lt 1) {$retur+="Java ikke fundet!"}Else{Write-host "Java fundet."}
	}
	$res=Get-ChildItem $drev\ -Recurse -ErrorAction SilentlyContinue|Where-Object {$_.fullname -match("$rm1") -or $_.fullname -match("$rm2")}| % { $_.FullName }
	If ($res.count -gt 0){
		$retur+="Der er fundet "+ $res.count + " Forekomster af filnavn '$rm1' / '$rm2' ! på drev $drev"
		foreach($r in $res){ if ($r -match("core")){$core+=$r}}
		if ($core.count -gt 0){$retur+="Heraf "+$core.count +" Core fundet."; $retur+=$core;$retur+="____________"}
		#Header
		$mHead="Der er fundet "+ $res.count + " Forekomster af filnavn '$rm1' / '$rm2' ! på drev $drev."
		#Body / Question
		$Lix=0
		$Lil=0
		#$r=$res[$Lix]
		foreach($r in $res){
	#Write-Host "lix: $Lix lil: $lil "  
			If($Lil % $antL -eq 0){
				If ($res.count -gt $antL) {[string]$mQuest="Max første $antL vist."}Else{[string]$mQuest=""}
				$Lil=0
			}
			If($Lix -le $antl){$mQuest+=$r +$EgoCrLf}

			If((($Lix+$Lil+1) -ge $res.count) -or ($Lil % $antL -eq $antL-1)){
				$mQuest+=" "+$EgoCrLf
				$mQuest+="Tryk Yes for at omdøbe til .$repl, tryk No for at slette, eller Cancel for at Liste Rename kommandoer!"
				#Popup box
				$wshell = New-Object -ComObject Wscript.Shell -ErrorAction Stop
				#Start Popup
				If($Lix -lt $antl){
					$rs=$wshell.Popup($mQuest,0,$mHead,3+32)
				}
				For($i=0;$i -lt $antL;$i++){
					If(($Lix+$i) -ge $res.count){Break}
					$ra=$res[$Lix+$i] -split("\.")
					$ra[-1]=$repl
					$rn=$ra -join(".")
					Switch ($rs){
						6	{Rename-Item -Path $res[$Lix+$i] -NewName $rn -force;$retur+=$res[$Lix+$i] + " omdøbt til $rn!"}
						7	{Remove-Item -Path $res[$Lix+$i] -force;$retur+=$res[$Lix+$i] + " Slettet!"}
						#3	{$retur+=$res[$Lix+$i] + " ignoreret!"}
						2	{$retur+="#Rename-Item -Path """+$res[$Lix+$i] +""" -NewName ""$rn"" -force"}
					}
				}
				#If(($Lix+$i) -ge $res.count){Break}
			$Lix+=$antL
			$Lil=-1
			}
		$Lil+=1
		}
		Return $retur
	}Else{ Write-Host "Der er ikke fundet nogen Forekomster af filnavn '$rm1' / '$rm2' ! på drev $drev"}
}
