<# 
.SYNOPSIS 
Find-Log4j.ps1 søger c:-disk igennem, ignorerer fejl og finder filer der indeholder 'Log4J' eller 'slf4j'
Script opdateret Mikael Veistrup-Vetlov @ 20220107
Script oprettet Mikael Veistrup-Vetlov @ 20220104

.Parameter drev 
Find-Log4j.ps1 kan vælge at checke andet <-drev> end c: hvis ønsket -drev f

.Parameter java 
Find-Log4j.ps1 kan vælge at checke om der er installeret Java på server, med parameter -java j

.DESCRIPTION 
Find-Log4j.ps1 bruger søgeargumenterne 'Log4J' og 'slf4j' for at søge på c:-drev efter filer hvor disse indeholdes i filnavn / sti 
Filerne bliver vist i popup vindue, og man kan vælge at omdøbe, slette eller Liste filerne
Listen kan pipes videre til fil for dokumentation i en lokal fil, via '>'
Hvis man vælger at liste, kan man åbne filen (notepad) og kopiere de forekomster man vil omdøbe (til powershell)

.EXAMPLE 
.\Find-Log4j.ps1 > .\log4j\$env:COMPUTERNAME.log 
Søger c:-drev igennem viser forekomster, spørger om filer skal omdøbes, slettes eller Listes.

.EXAMPLE 
.\Find-Log4j.ps1 -drev f -java j > .\log4j\$env:COMPUTERNAME.log 
Checker om der er installeret program: *java* +
Søger f:-drev igennem, viser forekomster af  'Log4J' eller 'slf4j', spørger om filer skal omdøbes, slettes eller Listes.

.NOTES
.LINK
