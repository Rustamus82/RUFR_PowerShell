.Parameter fil 
Her kan angives et sti/filnavn for en fil, der analyseres for xml parametre 
ingen <fil> betyder at scriptet lede efter \ps1\<SQLversion 20xx>\<Servernavn>\SqlInstParm.xml

.DESCRIPTION 
List-Xml.ps1 kan bruge argument <-fil> for at åbne en fil, læse evt. xml ind i PowerShell, og liste dette
Hvis der ikke angives noget argument, vil scriptet lede efter \ps1\<SQLversion 20xx>\<Servernavn>\SqlInstParm.xml
og liste xml fra dette dokument

.EXAMPLE 
.\List-Xml.ps1 -fil p:\miveps1\xml\parm.xml
Checker om der findes en fil <fil> og lister xml-parametre i denne
.EXAMPLE 
Dette kan angives med alias -sti for -fil, og der kan angives -kunroot y for kun at liste 1 level af xml struktur
.\List-Xml.ps1 -sti  p:\sqldba\ps1\2019\s-sqlasis01-t\SqlInstParm.xml -kunroot y

